function avg(array){
  let total= 0;
  for (let i = 0; i < array.length; i++){
    total += array[i]
  }
  return total/array.length

}

var array1 = [1 , 4 , 7]  //Output: 4
var array2 = [10, 5]  //Output: 7.5
var array3 = [1.5, 3, 2.5, 1]  //Output: 2


console.log(avg(array2));


